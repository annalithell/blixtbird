# blixtbird/blixtbird.py

"""
Main entry point for the Blixtbird Simulator.

This script initializes and runs the Blixtbird Shell, which provides
an interactive command-line interface for running decentralized
federated learning simulations.
"""

from blixtbird.cli import run_blixtbird_shell

def main():
    """Run the Fenics Shell."""
    run_blixtbird_shell()

if __name__ == "__main__":
    main()